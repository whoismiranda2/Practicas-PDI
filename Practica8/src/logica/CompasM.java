package logica;

/*
 *Esta clase contiene las máscaras del método de operador de gradiente compás
*/

public class CompasM {

    //prewit
    public static double[][][] prewitt() {
        return new double[][][]{
            {{1,1,-1},{1,-2,-1},{-1,1,-1}},   // E
            {{1,-1,-1},{1,-2,-1},{1,1,1}},   // NE
            {{-1,-1,-1},{1,-2,1},{1,1,1}},   // N
            {{-1,-1,1},{-1,-2,1},{1,1,1}},   // NW
            {{-1,1,1},{-1,-2,1},{-1,1,1}},   // W
            {{1,1,1},{-1,-2,1},{-1,-1,1}},   // SW
            {{1,1,1},{1,-2,1},{-1,-1,-1}},   // S
            {{1,1,1},{1,-2,-1},{1,-1,-1}}    // SE
        };
    }

    //kirsch
    public static double[][][] kirsch() {
        return new double[][][]{
            {{5,-3,-3},{5,0,-3},{5,-3,-3}},   // E
            {{-3,-3,-3},{5,0,-3},{5,5,-3}},   // NE
            {{-3,-3,-3},{-3,0,-3},{5,5,5}},   // N
            {{-3,-3,-3},{-3,0,5},{-3,5,5}},   // NW
            {{-3,-3,5},{-3,0,5},{-3,-3,5}},   // W
            {{-3,5,5},{-3,0,5},{-3,-3,-3}},   // SW
            {{5,5,5},{-3,0,-3},{-3,-3,-3}},   // S
            {{5,5,-3},{5,0,-3},{-3,-3,-3}}    // SE
        };
    }
    
    //robinson3
    public static double[][][] robinson3() {
        return new double[][][]{
            {{1,0,-1},{1,0,-1},{1,0,-1}},   // E
            {{0,-1,-1},{1,0,-1},{1,1,0}},   // NE
            {{-1,-1,-1},{0,0,0},{1,1,1}},   // N
            {{-1,-1,0},{-1,0,1},{0,1,1}},   // NW
            {{-1,0,1},{-1,0,1},{-1,0,1}},   // W
            {{0,1,1},{-1,0,1},{-1,-1,0}},   // SW
            {{1,1,1},{0,0,0},{-1,-1,-1}},   // S
            {{1,1,0},{1,0,-1},{0,-1,-1}}    // SE
        };
    }
    
    //robinson5
    public static double[][][] robinson5() {
        return new double[][][]{
            {{1,0,-1},{2,0,-2},{1,0,-1}},   // E
            {{0,-1,-2},{1,0,-1},{2,1,0}},   // NE
            {{-1,-2,-1},{0,0,0},{1,2,1}},   // N
            {{-2,-1,0},{-1,0,1},{0,1,2}},   // NW
            {{-1,0,1},{-2,0,2},{-1,0,1}},   // W
            {{0,1,2},{-1,0,1},{-2,-1,0}},   // SW
            {{1,2,1},{0,0,0},{-1,-2,-1}},   // S
            {{2,1,0},{1,0,-1},{0,-1,-2}}    // SE
        };
    }
}